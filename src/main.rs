mod aws_services;

use aws_services::{athena, cloudwatch, dynamodb, eventbridge, glue, lakeformation, lambda, profiles, rds, redshift, s3};

fn main() {
    tauri::Builder::default()
        .invoke_handler(tauri::generate_handler![
            profiles::get_profiles,
            s3::list_buckets,
            s3::list_objects,
            s3::get_object_detail,
            s3::generate_presigned_url,
            s3::search_objects,
            s3::upload_object,
            s3::download_object,
            s3::delete_object,
            s3::copy_object,
            s3::move_object,
            dynamodb::dynamo_list_tables,
            dynamodb::describe_table,
            dynamodb::scan_table,
            dynamodb::dynamo_query_table,
            glue::glue_list_databases,
            glue::glue_list_tables,
            glue::glue_list_jobs,
            glue::glue_list_crawlers,
            glue::glue_get_job_runs,
            glue::glue_start_job,
            glue::glue_start_crawler,
            glue::glue_get_job_script,
            glue::glue_list_triggers,
            glue::glue_start_trigger,
            glue::glue_stop_trigger,
            athena::list_executions,
            athena::athena_list_databases,
            athena::athena_list_saved_queries,
            athena::start_query,
            athena::athena_get_query_status,
            athena::get_results,
            athena::athena_download_results,
            athena::athena_generate_results_link,
            rds::rds_list_instances,
            rds::rds_list_clusters,
            rds::rds_list_snapshots,
            rds::rds_stop_instance,
            rds::rds_start_instance,
            redshift::redshift_list_clusters,
            redshift::redshift_list_snapshots,
            redshift::redshift_list_tables,
            redshift::redshift_execute_query,
            redshift::redshift_get_query_status,
            redshift::redshift_get_query_results,
            lambda::lambda_list_functions,
            lambda::lambda_get_function_code,
            lambda::lambda_invoke,
            lambda::lambda_get_recent_logs,
            lambda::lambda_download_and_extract,
            cloudwatch::cw_list_alarms,
            cloudwatch::cw_list_log_groups,
            cloudwatch::cw_list_log_streams,
            cloudwatch::cw_get_log_events,
            cloudwatch::cw_filter_logs,
            cloudwatch::cw_list_metrics,
            eventbridge::eb_list_buses,
            eventbridge::eb_list_rules,
            eventbridge::eb_list_targets,
            eventbridge::eb_enable_rule,
            eventbridge::eb_disable_rule,
            eventbridge::eb_put_event,
            lakeformation::lf_list_databases,
            lakeformation::lf_list_tables,
            lakeformation::lf_list_permissions,
            lakeformation::lf_get_settings,
            lakeformation::lf_list_resources,
            lakeformation::lf_list_tags,
            lakeformation::lf_list_tag_permissions,
        ])
        .run(tauri::generate_context!())
        .expect("error while running tauri application");
}
